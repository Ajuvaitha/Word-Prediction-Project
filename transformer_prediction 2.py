def compute_accuracy(model, loader):
    model.eval()
    correct = 0
    total = 0

    with torch.no_grad():
        for x, y in loader:
            x, y = x.to(device), y.to(device)
            out = model(x)

            preds = out.argmax(dim=-1)

            correct += (preds == y).sum().item()
            total += y.numel()

